export type CommoditySector = 'precious' | 'base' | 'energy' | 'agri' | 'softs' | 'indices';
export type Exchange = 'COMEX' | 'NYMEX' | 'LME' | 'ICE' | 'CBOT' | 'CME' | 'MCX' | 'BURSA' | 'SGX' | 'DCE' | 'CBOE';

export interface CommoditySpec {
    id: string;
    name: string;
    symbol: string;
    sector: CommoditySector;
    exchange: Exchange;
    currency: string;
    unit: string;
    yahooTicker?: string;        // Yahoo Finance ticker for price fetching
    twelveDataSymbol?: string;   // Twelve Data symbol
    googleFinanceSymbol?: string; // Google Finance symbol (for MCX: GOLD, SILVER, etc.)
    lotSize?: number;
    tickSize?: number;
    tradingHours?: string;
    description?: string;

    // MCX mapping (if this is a global commodity with an MCX equivalent)
    mcxEquivalent?: string;      // ID of the MCX equivalent

    // Global mapping (if this is an MCX commodity)
    globalEquivalent?: string;   // ID of the global benchmark

    // For premium/discount calculation
    conversionFactor?: number;   // e.g., 31.1035 for oz→gram
    importDuty?: number;         // percentage
}

